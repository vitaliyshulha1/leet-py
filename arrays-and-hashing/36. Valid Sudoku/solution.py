# Импортируем List из typing — нужен для аннотаций типов в Python 3.8 и ниже
from typing import List


# Объявляем класс Solution — стандартный шаблон LeetCode
class Solution:

    # Метод принимает board: двумерный список 9x9 из строк ('1'..'9' или '.')
    # Возвращает bool: True если доска валидна, False если есть нарушение
    def isValidSudoku(self, board: List[List[str]]) -> bool:

        # Создаём 9 множеств для строк — rows[i] хранит числа уже виденные в строке i
        rows = [set() for _ in range(9)]

        # Создаём 9 множеств для столбцов — cols[j] хранит числа виденные в столбце j
        cols = [set() for _ in range(9)]

        # Создаём 9 множеств для квадратов 3×3 — boxes[k] для квадрата с индексом k
        boxes = [set() for _ in range(9)]

        # Перебираем каждую строку r от 0 до 8
        for r in range(9):

            # Перебираем каждый столбец c от 0 до 8
            for c in range(9):

                # Берём значение текущей клетки — строка '1'..'9' или '.'
                val = board[r][c]

                # Пустая клетка — пропускаем, она ни на что не влияет
                if val == '.':
                    continue

                # Вычисляем индекс квадрата 3×3:
                # r//3 → номер блок-строки (0,1,2), c//3 → номер блок-столбца (0,1,2)
                # Результат: уникальный индекс от 0 до 8
                box_index = (r // 3) * 3 + (c // 3)

                # Проверяем строку r — если val уже встречалось, это дубликат
                if val in rows[r]:
                    return False  # Нарушение правила строки

                # Проверяем столбец c — если val уже встречалось, это дубликат
                if val in cols[c]:
                    return False  # Нарушение правила столбца

                # Проверяем квадрат — если val уже встречалось, это дубликат
                if val in boxes[box_index]:
                    return False  # Нарушение правила квадрата 3×3

                # Всё чисто — регистрируем val в множестве строки r
                rows[r].add(val)

                # Регистрируем val в множестве столбца c
                cols[c].add(val)

                # Регистрируем val в множестве квадрата box_index
                boxes[box_index].add(val)

        # Прошли всю доску без нарушений — доска валидна
        return True


# Создаём объект класса Solution
sol = Solution()

# Тест 1: валидная частично заполненная доска
board1 = [
    ["5","3",".",".","7",".",".",".","."],
    ["6",".",".","1","9","5",".",".","."],
    [".","9","8",".",".",".",".","6","."],
    ["8",".",".",".","6",".",".",".","3"],
    ["4",".",".","8",".","3",".",".","1"],
    ["7",".",".",".","2",".",".",".","6"],
    [".","6",".",".",".",".","2","8","."],
    [".",".",".","4","1","9",".",".","5"],
    [".",".",".",".","8",".",".","7","9"]
]
print(sol.isValidSudoku(board1))  # True

